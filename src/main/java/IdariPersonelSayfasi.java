    private void akademisyenguncelle_butonuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_akademisyenguncelle_butonuActionPerformed
        String ders_id=(String)akademisyenguncelledersid_alani.getText();
        String kisi_adi=akademisyenguncellead_alani.getText();
        String soyadi=akademisyenguncellesoyad_alani.getText();
        String bolum_adi=akademisyenguncellebolumadi_alani.getText();
        String fakulte_adi=akademisyenguncellefakulteadi_alani.getText();
        String e_posta=akademisyenguncelleeposta_alani.getText();
        String kullanici_adi=akademisyenguncellekullaniciadi_alani.getText();
        String parola=akademisyenguncelleparola_alani.getText();
        
        int selectedrow=akademisyen_tablosu.getSelectedRow();
        
        if(selectedrow==-1)
        {
            //tablo boşsa
            if(model.getRowCount()==0)
            {
                guncellemesaj_yazisi_alani.setText("Akademisyenler Tablosu Şuanda Boş.");
            }
            else//tablo boş değil satır seçilmemiş
            {
                guncellemesaj_yazisi_alani.setText("Lütfen Güncellenecek Bir Akademisyen Seçiniz.");
            }
        }
        else//satır seçilmiş
        {
            int id=(int)model.getValueAt(selectedrow, 0);
            
            idariPersonelIslemler.akademisyenGuncelle(id,ders_id,kisi_adi,soyadi,bolum_adi,fakulte_adi,e_posta,kullanici_adi,parola);
            
            akademisyenGoruntule();
            
            guncellemesaj_yazisi_alani.setText("Akademisyen Başarıyla Güncellendi.");
            
        }
    }//GEN-LAST:event_akademisyenguncelle_butonuActionPerformed

