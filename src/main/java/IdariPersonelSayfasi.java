    public void akademisyenGoruntule()
    {
        model.setRowCount(0);
        
        ArrayList<Akademisyen> akademisyenler=new ArrayList<Akademisyen>();
        
        akademisyenler=idariPersonelIslemler.akademisyenleriGetir();
        
        if(akademisyenler != null)
        {
            for(Akademisyen akademisyen : akademisyenler)
            {
                Object[] eklenecek={akademisyen.getId(),akademisyen.getDers_id(),akademisyen.getKisi_adi(),akademisyen.getSoyadi(),akademisyen.getBolum_adi(),akademisyen.getFakulte_adi(),akademisyen.getE_posta(),akademisyen.getKullanici_adi(),akademisyen.getParola()};
            
                model.addRow(eklenecek);
            }
        }
    }


    
    
    public void ogrenciGoruntule()
    {
        model.setRowCount(0);
        
        ArrayList<Ogrenci> ogrenciler=new ArrayList<Ogrenci>();
        
        ogrenciler=idariPersonelIslemler.ogrencileriGetir();
        
        if(ogrenciler != null)
        {
            for(Ogrenci ogrenci : ogrenciler)
            {
                Object[] eklenecek={ogrenci.getId(),ogrenci.getKisi_adi(),ogrenci.getSoyadi(),ogrenci.getBolum_adi(),ogrenci.getFakulte_adi(),ogrenci.getCep_no(),ogrenci.getE_posta(),ogrenci.getFotograf(),ogrenci.getKullanici_adi(),ogrenci.getParola(),ogrenci.getOkul_no(),ogrenci.getTercih_sirasi(),ogrenci.getYks_puani(),ogrenci.getKayit_tarihi()};
            
                model.addRow(eklenecek);
            }
        }
    }
    
    
    
