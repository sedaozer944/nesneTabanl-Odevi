    private void idaripersonelgiris_butonuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_idaripersonelgiris_butonuActionPerformed
        
        mesaj_yazisi_labeli.setText("");
        String kullanici_adi=kullanici_adi_field.getText();
        String parola=new String(sifre_field.getPassword());
        
        boolean girisBasarili=idariPersonelIslemleri.girisYap(kullanici_adi,parola);
        
        if(girisBasarili)
        {
            mesaj_yazisi_labeli.setText("Giriş Başarılı.");
            setVisible(false);
            new IdariPersonelSayfasi().setVisible(true);//IdariPersonelSayfasi nı açacak
        }
        else
        {
            mesaj_yazisi_labeli.setText("Giriş Başarısız. Lütfen Tekrar Deneyin.");
        }
        
        
    }//GEN-LAST:event_idaripersonelgiris_butonuActionPerformed

