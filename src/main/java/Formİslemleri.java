    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       String id=(String)id_alani.getText();
       String kisi_adi=(String)kisi_adi_alani.getText();
       String soyadi=(String)soyad_alani.getText();
       String bolum_adi=(String)bolum_alani.getText();
       String fakulte_adi=(String)fakulte_alani.getText();
       String e_posta=(String)eposta_alani.getText();
       String cep_no =(String)cep_no_alani.getText();
       String parola=(String)parola_alani.getText();
       String url="jdbc:mysql://"+ObsDatabase.host+":"+ObsDatabase.port+"/"+ObsDatabase.db_ismi+ "?useUnicode=true&characterEncoding=utf8";
       String sorgu ="update ogrenci set kisi_adi = ? , soyadi = ? , bolum_adi = ? , fakulte_adi = ? , e_posta = ? , cep_no = ? , parola = ?  where id = ? ";
       try{
         try {
            
             
              preparedStatement=con.prepareStatement(sorgu);
             preparedStatement.setString(1,kisi_adi );
             preparedStatement.setString(2,soyadi );
             preparedStatement.setString(3,bolum_adi );
             preparedStatement.setString(4,fakulte_adi);
             preparedStatement.setString(5,e_posta);
             preparedStatement.setString(6, cep_no);
             preparedStatement.setString(7, parola);
             preparedStatement.setString(8, id);
             
             preparedStatement.executeUpdate();
             JOptionPane.showMessageDialog(null, "Bilgileriniz Guncellendi");
    
          
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, ex);
            
        }
     }catch(Exception e){
         JOptionPane.showMessageDialog(null, "baglama ile ilgili sıkıntı var");
     }
    
        
    }//GEN-LAST:event_jButton3ActionPerformed

